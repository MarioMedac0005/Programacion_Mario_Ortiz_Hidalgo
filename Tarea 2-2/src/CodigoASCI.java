public class CodigoASCI {
    public static void main(String[] args) throws Exception {

        /*
         * Usar bucles para ir incrementando el numero e ir transformando el int a char
         * para mostrar por pantalla la letra correspondiente
         */

        for (int i = 0; i <= 128; i++) {

            System.out.println((char) (i));

        }

    }
}
