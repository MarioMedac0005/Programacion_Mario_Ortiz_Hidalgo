public class Arrays1 {
    public static void main(String[] args) throws Exception {

        char[] arrays = { 'i', 'n', 'f', 'o', 'r', 'm', 'a', 't', 'i', 'c', 'a' };

        for (int i = 0; i < arrays.length; i++) {
            if (arrays[i] == 'm') {
                System.out.println(i);

            }
        }

    }
}